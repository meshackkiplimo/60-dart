//arithmethic operations in dart
void main() {
  // addition
  int a = 10;
  int b = 20;
  var sum = a + b;
  print(sum); // prints 30

  // sbrtaction
  int c = 10;
  int d = 20;
  var diff = c - d;
  print(diff); // prints -10

  // multiplication
  int e = 8;
  int f = 2;
  var product = e * f;
  print(product); // prints 16

  // division
  int g = 10;
  int h = 2;
  var quotient = g / h;
  print(quotient);

  // Using ~/ to divide
  int i = 10;
  int j = 2;
  var quotient2 = i ~/ j;
  print(quotient2);

  //modullars
  int k = 10;
  int l = 3;
  var modulus = k % l;
  print(modulus); // prints 0

}

//variables in dart
// Dart is a statically typed language, which means that the data type of a variable must be declared
// before the variable is used. The data type of a variable can be a primitive type (such
// as int, double, bool, String, etc.) or a class type.
//
// Here are some examples of variables in Dart:
//

// they ae used to store data

void main() {
  // declare and initialize a variable
  //var
  var name = "John Doe"; // variable name with value "John Doe"
  final population = 3;
  const Pi = 3.14;

  print(name); // prints "John Doe"
  print(population);
  print(Pi);
}
